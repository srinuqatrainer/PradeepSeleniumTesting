package com.BasicJava;

public class VariableExample {

	public static void main(String[] args) {
		
		int a=20;
		a=30;
		
		String b="Hello";
		double c=10.123;
		
		// a variable can save only one value at a time
		// a variable should be always declared with the dataType
		// a variable is always a Temp memory
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);

	}

}
